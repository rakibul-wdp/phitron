#include <bits/stdc++.h>
using namespace std;

int main()
{
  int n;
  cin >> n;

  switch (n)
  {
  case 1:
    cout << "one" << endl;
    break;
  case 2:
    cout << "two" << endl;
    break;
  case 3:
    cout << "three" << endl;
    break;

  default:
    cout << "nothing match" << endl;
  }

  return 0;
}